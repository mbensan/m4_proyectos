public class Calculadora implements IOperaciones {

  public double suma(double x, double y) {
    return x + y;
  }

  public double resta(double x, double y) {
    return x - y;
  }

  public double mult(double x, double y) {
    return x * y;
  }

  public double div(double x, double y) {
    return x / y;
  }
}
